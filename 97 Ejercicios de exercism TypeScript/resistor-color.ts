export const colorCode = (color: string) => {
    // Devuelve el índice del color en el arreglo COLORS usando el método indexOf().
    return COLORS.indexOf(color);
}

/**
 * Arreglo de colores estándar.
 */
export const COLORS = [
    'black',
    'brown',
    'red',
    'orange',
    'yellow',
    'green',
    'blue',
    'violet',
    'grey',
    'white',
]